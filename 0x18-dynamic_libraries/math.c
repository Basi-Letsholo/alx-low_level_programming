#include <stdio.h>
#include "math.h"

/**
 * add - finds sum of 2 ints
 * @a: 1st int
 * @b: 2nd int
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts b from a
 * @a: first int
 * @b: 2nd int
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiples a and b
 * @a: first int
 * @b: 2nd int
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides a and b
 * @a: first int
 * @b: 2nd int
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds modulus (remainder when a is divided by b)
 * @a: first int
 * @b: 2nd int
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
